
public class TasquesUD4_2 {
	/**
	 * Programa que realiza diferentes operaciones
	 * @author Joel
	 */
	public static void main(String[] args) {
		int n = 8;
		double a = 9.5;
		char c = 'b';
		double suma = n + a;
		double resta = a - n;
		
		System.out.println("Variable N = " + n);
		System.out.println("Variable A = " + a);
		System.out.println("Variable C = " + c);
		System.out.println(n + " + " + a + " = " + suma);
		System.out.println(a + " + " + n + " = " + resta);
		System.out.println("El valor numérico del carácter " + c + " = " + (int)c);
	}

}
